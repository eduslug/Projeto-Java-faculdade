    private void jTableMoradorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMoradorMouseClicked
      //mouse
        //clickted
        //alterar travez de mouse
        String nome_morador=""+jTableMorador.getValueAt(jTableMorador.getSelectedRow(),1);
        conex.conexao();
        conex.executaSql("select * from contas where nome_morador='"+nome_morador+"'");
         try {
            conex.rs.first();
           jTextCodigo.setText(String.valueOf(conex.rs.getInt("cod_contas")));
           jTextFieldNomeMoradorCont.setText(conex.rs.getString("nome_morador"));
           jTextFieldNumeroCasa1.setText(conex.rs.getString("numero_casa"));
           jTextFieldContaApagar.setText(conex.rs.getString("contas_apagar"));
           jTextFieldContasPagas.setText(conex.rs.getString("contas_pagas"));
           
           
           
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro ao selecionar dados"+ex);
        }
         jTextFieldNomeMoradorCont.setEnabled(false);
         jTextFieldNumeroCasa1.setEnabled(false);
         jTextFieldContaApagar.setEnabled(false);
         jTextFieldContasPagas.setEnabled(false);
         
        jButtonEditar.setEnabled(true);
        jButtonNovo.setEnabled(false);
        jButtonExcluir.setEnabled(true);
        jButtonCancelar.setEnabled(true);
        jButtonLimpar.setEnabled(false);
    }//GEN-LAST:event_jTableMoradorMouseClicked

    public void preencherTabela(String Sql)
    {
    ArrayList dados=new ArrayList();
    
    String [] colunas=new String[]{"ID","Nome Morador","NÃºmero da casa","Contas Apagar","contas Pagas"};
    
    conex.conexao();
    conex.executaSql(Sql);
    
    try{
    
    conex.rs.first();
    
    do {
    
    dados.add(new Object[]{conex.rs.getInt("cod_contas"),conex.rs.getString("nome_morador"),conex.rs.getString("numero_casa"),conex.rs.getString("contas_apagar"),conex.rs.getString("contas_pagas")});
       
    
       }while(conex.rs.next());
    
    }catch(SQLException ex)
       {
        JOptionPane.showMessageDialog(rootPane,"Erro ao preencher ArrayLyst"+ex);
       }
        ModeloTabelaContas modelo=new ModeloTabelaContas(dados,colunas);
        
        jTableMorador.setModel(modelo);
        
         jTableMorador.getColumnModel().getColumn(0).setPreferredWidth(28);
         jTableMorador.getColumnModel().getColumn(0).setResizable(false);
         
         jTableMorador.getColumnModel().getColumn(1).setPreferredWidth(203);
         jTableMorador.getColumnModel().getColumn(1).setResizable(false);
         
         jTableMorador.getColumnModel().getColumn(2).setPreferredWidth(103);
         jTableMorador.getColumnModel().getColumn(2).setResizable(false);
         
         jTableMorador.getColumnModel().getColumn(3).setPreferredWidth(123);
         jTableMorador.getColumnModel().getColumn(3).setResizable(false);
         
        jTableMorador.getColumnModel().getColumn(4).setPreferredWidth(133);
         jTableMorador.getColumnModel().getColumn(4).setResizable(false);
       
        
        
        jTableMorador.setAutoResizeMode(jTableMorador.AUTO_RESIZE_OFF);
        jTableMorador.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        jTableMorador.getTableHeader().setReorderingAllowed(false);
        
        
        
    conex.Desconectar();
    }
    
    
